#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int i;

	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && s1[i] && s2[i] && (size_t)i < n)
		i++;
	if ((size_t)i == n)
		return (0);
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
